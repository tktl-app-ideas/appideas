class IdeasController < ApplicationController
  before_action :set_idea, only: [:show, :edit, :update, :destroy]

  # GET /ideas
  # GET /ideas.json
  def index
    @ideas = Idea.all
  end

  # GET /ideas/1
  # GET /ideas/1.json
  def show
    @keywords = Idea.find(params[:id]).keywords
  end

  # GET /ideas/new
  def new
    @idea = Idea.new
    @keywords = Keyword.all
  end

  # GET /ideas/1/edit
  def edit
  end

